// JS is dynamically typed language
// JS is not type safe language

// data types

// number
let num = 200
console.log(`num = ${num}, type = ${typeof num}`)

// string
num = 'test'
console.log(`num = ${num}, type = ${typeof num}`)

// boolean
num = true
console.log(`num = ${num}, type = ${typeof num}`)

// undefined
num = undefined
console.log(`num = ${num}, type = ${typeof num}`)

// object
num = {}
console.log(`num = ${num}, type = ${typeof num}`)

// number
num = 300
console.log(`num = ${num}, type = ${typeof num}`)
